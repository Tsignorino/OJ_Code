using namespace std;
using ll = long long;

static constexpr int MOD = 1'000'000'007;

void solve() {
    int n;
    cin >> n;

    const string s = "aeiou";

    string ans;
    for (int i = 0; i < n; ++i) {
        ans.push_back(s[i % 5]);
    }

    ranges::sort(ans);

    cout << ans << "\n";
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int T;
    cin >> T;
    while (T--) {
        solve();
    }

    return 0;
}